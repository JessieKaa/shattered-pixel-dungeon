#!/usr/bin/env node
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = dirname(__filename)
const REPO_ROOT = resolve(__dirname, '..', '..', '..')
const SRC = resolve(REPO_ROOT, 'core/src/main/assets/messages/items/items_zh.properties')
const OUT_JSON = resolve(__dirname, '..', 'frontend/src/data/item-labels-zh.json')
const OUT_README = resolve(__dirname, '..', 'frontend/src/data/item-labels-zh.README.md')

const LINE_RE = /^([\w$]+\.[\w$.]+)\.name=(.+)$/

const text = readFileSync(SRC, 'utf8')
const labels = {}
for (const line of text.split(/\r?\n/)) {
  const m = line.match(LINE_RE)
  if (!m) continue
  const key = m[1].toLowerCase()
  const raw = m[2]
  const value = raw.replace(/\s*%s\s*$/, '')
  labels[key] = value
}

mkdirSync(dirname(OUT_JSON), { recursive: true })
writeFileSync(OUT_JSON, JSON.stringify(labels, null, 2) + '\n', 'utf8')

const readme = `# item-labels-zh.json

AUTO-GENERATED by \`extract-item-labels.mjs\`. DO NOT EDIT.
Source: \`core/src/main/assets/messages/items/items_zh.properties\`.
Regenerate: \`node tools/save-editor/scripts/extract-item-labels.mjs\`

- Only \`*.name=\` rows are extracted (${Object.keys(labels).length} entries).
- Trailing \`%s\` placeholders (curse/glyph/enchantment adjectives) are stripped.
- Keys are lowercased to match FQCN → properties key conversion in \`useItemLabel.ts\`.
`
writeFileSync(OUT_README, readme, 'utf8')

console.log(`Wrote ${Object.keys(labels).length} entries → ${OUT_JSON}`)
console.log(`Wrote README → ${OUT_README}`)
